public class bunnyEars {
public int bunnyEars(int bunnies) {
  if (bunnies == 0) 
  return 0;
  return bunnies * 2;

}
public void main (String [] args) {
	System.out.println(bunnyEars(1));
	System.out.println(bunnyEars(2));
	System.out.println(bunnyEars(3));
}
}
